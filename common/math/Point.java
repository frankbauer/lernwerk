/**
 * A point in 2D space.
 */
class Point {
    /**
     * The x-Coordinate
     */
    public final int x;
    /**
     * The y-Coordinate
     */
    public final int y;

    /**
     * Creates a new Point
     * 
     * @param x The x-Coordinate
     * @param y The y-Coordinate
     */
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
